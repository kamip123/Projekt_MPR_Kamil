package library.repos.impl;

import java.sql.Connection;
import java.sql.PreparedStatement;
import java.sql.ResultSet;
import java.sql.SQLException;
import java.util.ArrayList;
import java.util.List;

import library.domain.Ogloszenie;
import library.mappers.IMapper;
import library.repos.IOgloszenieRepository;
import library.uow.IUnitOfWork;

public class OgloszenieRepository extends RepositoryBase<Ogloszenie> implements IOgloszenieRepository{
	String selectCenaOdSql = "SELECT * FROM ogloszenie WHERE cena<?";
	PreparedStatement selectCenaOd;
	
	public OgloszenieRepository(Connection connection,IMapper<Ogloszenie> mapper, IUnitOfWork uow) throws SQLException{
		super(connection,mapper, uow);
		selectCenaOd = connection.prepareStatement(selectCenaOdSql);
	}

	@Override
	protected String getUpdateQuerySql() {
		return "UPDATE ogloszenie SET (tytul, opis, cena, dataUmieszczenia, dataWygasniecia, idSamochodu, idSprzedawcy) = (?,?,?,?,?,?,?) WHERE id=?";
	}

	@Override
	protected String getInsertQuerySql() {
		return "INSERT INTO ogloszenie(tytul, opis, cena, dataUmieszczenia, dataWygasniecia, idSamochodu, idSprzedawcy) VALUES (?,?,?,?,?,?,?)";
	}

	@Override
	protected String getTableName() {
		return "ogloszenie";
	}

	@Override
	protected String createTableStatementSql() {
		return "CREATE TABLE ogloszenie("
		+ "id bigint GENERATED BY DEFAULT AS IDENTITY,"
		+ "tytul varchar(20),"
		+ "opis varchar(500),"
		+ "cena bigint,"
		+ "dataUmieszczenia date,"
		+ "dataWygasniecia date,"
		+ "idSamochodu bigint,"
		+ "idSprzedawcy bigint"
		+ ")";
	}

	@Override
	protected void setUpdate(Ogloszenie ogloszenie) throws SQLException {
		update.setString(1, ogloszenie.getTytul());
		update.setString(2, ogloszenie.getOpis());
		update.setInt(3, ogloszenie.getCena());
		update.setDate(4, ogloszenie.getDataUmieszczenia());
		update.setDate(5, ogloszenie.getDataWygasniecia());
		update.setInt(6, ogloszenie.getIdSamochodu());
		update.setInt(7, ogloszenie.getIdSprzedawcy());
		update.setInt(8, ogloszenie.getId());
		
	}

	@Override
	protected void setInsert(Ogloszenie ogloszenie) throws SQLException {
		insert.setString(1, ogloszenie.getTytul());
		insert.setString(2, ogloszenie.getOpis());
		insert.setInt(3, ogloszenie.getCena());
		insert.setDate(4, ogloszenie.getDataUmieszczenia());
		insert.setDate(5, ogloszenie.getDataWygasniecia());
		insert.setInt(6, ogloszenie.getIdSamochodu());
		insert.setInt(7, ogloszenie.getIdSprzedawcy());
		
	}
	
	@Override
	public List<Ogloszenie> cenaDo(int cena) {
		List<Ogloszenie> result = new ArrayList<Ogloszenie>();
		try {
			selectCenaOd.setInt(1, cena);
			ResultSet rs = selectCenaOd.executeQuery();
			while(rs.next()) result.add(mapper.map(rs));
		} catch (SQLException e) {
			e.printStackTrace();
		}
		return result;
	}
}
